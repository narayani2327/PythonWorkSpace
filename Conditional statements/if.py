"""Largest number comparison program."""


def main():
    """Compare two numbers and print the larger one."""
    numa = 10
    numb = 20

    if numa > numb:
        print("numa is greater than numb")
    elif numa < numb:
        print("numb is greater than numa")
    else:
        print("numa is equal to numb")


if __name__ == "__main__":
    main()
